"""
문제
예제를 보고 규칙을 유추한 뒤에 별을 찍어 보세요.

입력
첫째 줄에 N이 주어진다. N은 항상 3의 제곱꼴인 수이다. (3, 9, 27, ...) (N=3^k, 1 ≤ k < 8)

출력
첫째 줄부터 N번째 줄까지 별을 출력한다.

예제 입력 1
27 -> 3**3
9 -> 3**2
3 -> 3**1

예제 출력 1
***************************
* ** ** ** ** ** ** ** ** *
***************************
***   ******   ******   ***
* *   * ** *   * ** *   * *
***   ******   ******   ***
***************************
* ** ** ** ** ** ** ** ** *
***************************
*********         *********
* ** ** *         * ** ** *
*********         *********
***   ***         ***   ***
* *   * *         * *   * *
***   ***         ***   ***
*********         *********
* ** ** *         * ** ** *
*********         *********
***************************
* ** ** ** ** ** ** ** ** *
***************************
***   ******   ******   ***
* *   * ** *   * ** *   * *
***   ******   ******   ***
***************************
* ** ** ** ** ** ** ** ** *
***************************

"""


def draw(x, y, num, matrix):
    if num == 1:
        matrix[x][y] = '*'
        return

    div = num // 3
    for i in range(3):
        for j in range(3):
            if i == 1 and j == 1:
                pass
            else:
                draw(x + (i * div), y + (j * div), div, matrix)


if __name__ == '__main__':
    N = int(input())
    matrix = [[' '] * N for i in range(N)]
    draw(0, 0, N, matrix)

    for i in range(N):
        string = ''
        for j in range(N):
            string += matrix[i][j]
        print(string)
